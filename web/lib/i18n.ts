const CHAR_REGEX = new RegExp(
  `[${Object.keys(CHAR_REPLACEMENTS).join("")}]`,
  "g",
);

const applyReplacements = (
  value: string,
  replacements: Array<[string, string]>,
): string => {
  return replacements.reduce((current, [from, to]) => {
    if (from === "ç®—æ³•" && to === "æ¼”ç®—æ³•") {
      const placeholder = "__ALGORITHM__";
      const withPlaceholder = current.replaceAll("æ¼”ç®—æ³•", placeholder);
      return withPlaceholder
        .replaceAll(from, to)
        .replaceAll(placeholder, "æ¼”ç®—æ³•");
    }
    return current.replaceAll(from, to);
  }, value);
};

const applyCharMap = (value: string): string => {
  return value.replace(CHAR_REGEX, (char) => CHAR_REPLACEMENTS[char] || char);
};

const toTraditional = (
  value: string,
  variant: "zh-TW" | "zh-HK",
): string => {
  const wordReplacements =
    variant === "zh-TW" ? WORD_REPLACEMENTS_TW : WORD_REPLACEMENTS_HK;
  const withWords = applyReplacements(value, wordReplacements);
  return applyCharMap(withWords);
};

const convertTranslations = (
  source: TranslationMap,
  variant: "zh-TW" | "zh-HK",
): TranslationMap => {
  const converted: TranslationMap = {};
  for (const [key, value] of Object.entries(source)) {
    converted[key] = toTraditional(value, variant);
  }
  return converted;
};

