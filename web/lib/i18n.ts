export function getTranslation(lang: Language, key: string): string {
  const override = runtimeOverrides[lang];
  if (override && key in override) {
    return override[key] || key;
  }
  if (lang === "zh-TW" || lang === "zh-HK") {
    const source = translations["zh-CN"] || translations.en;
    const value = source[key] || translations.en[key] || key;
    return toTraditional(value, lang);
  }
  const dict = translations[lang] || translations.en;
  return dict[key] || key;
}

